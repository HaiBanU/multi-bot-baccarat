def expand_scenarios(scenarios, factor=4):
    expanded = {}
    for category, messages in scenarios.items():
        new_messages = list(messages)
        for _ in range(factor - 1):
            for msg in messages:
                variants = [f"{msg} anh em ạ.", f"Thật sự, {msg[0].lower()}{msg[1:]}", f"{msg}, quá logic!", f"Quan điểm của em là, {msg[0].lower()}{msg[1:]}"]
                new_messages.extend(variants)
        expanded[category] = list(set(new_messages))
    return expanded

SCENARIOS_SINH_VIEN = expand_scenarios(SCENARIOS_SINH_VIEN)